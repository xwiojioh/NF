//------------------------------------------------------------------------------
ausf_bcf::ausf_bcf(ausf_event& ev, const std::string& instance_id)
    : m_event_sub(ev), ausf_instance_id(instance_id) {
  Logger::ausf_app().debug(
      "[AUSF][BCF-REG] BCF client initialized with instance ID: %s",
      ausf_instance_id.c_str());
}

//------------------------------------------------------------------------------
ausf_bcf::~ausf_bcf() {
  Logger::ausf_app().debug("Delete AUSF_BCF instance...");
}

//------------------------------------------------------------------------------
bool ausf_bcf::read_nf_instance_id_from_file(std::string& nf_instance_id) {
  Logger::ausf_app().debug(
      "Reading nfInstanceId from extended profile file: %s",
      ausf_cfg.extended_profile_path.c_str());

  try {
    std::ifstream file(ausf_cfg.extended_profile_path);
    if (!file.is_open()) {
      Logger::ausf_app().warn(
          "Extended profile file not found: %s",
          ausf_cfg.extended_profile_path.c_str());
      return false;
    }

    nlohmann::json profile;
    file >> profile;
    file.close();

    if (profile.contains("nfInstanceId")) {
      nf_instance_id = profile["nfInstanceId"].get<std::string>();
      Logger::ausf_app().debug(
          "Read nfInstanceId from extended profile: %s", nf_instance_id.c_str());
      return true;
    } else {
      Logger::ausf_app().warn("nfInstanceId not found in extended profile");
      return false;
    }
  } catch (const std::exception& e) {
    Logger::ausf_app().warn(
        "Exception while reading nfInstanceId from file: %s", e.what());
    return false;
  }
}

//------------------------------------------------------------------------------
bool ausf_bcf::read_extended_profile_from_file(nlohmann::json& profile) {
  Logger::ausf_app().debug(
      "Reading extended profile from file: %s",
      ausf_cfg.extended_profile_path.c_str());

  try {
    std::ifstream file(ausf_cfg.extended_profile_path);
    if (!file.is_open()) {
      Logger::ausf_app().error(
          "Failed to open extended profile file: %s",
          ausf_cfg.extended_profile_path.c_str());
      return false;
    }

    file >> profile;
    file.close();

    Logger::ausf_app().info("Successfully read extended profile from file");
    
    // Log DID if present (similar to AMF style)
    if (profile.contains("did")) {
      Logger::ausf_app().info(
          "Extended profile contains DID: %s",
          profile["did"].get<std::string>().c_str());
    }

    return true;
  } catch (const std::exception& e) {
    Logger::ausf_app().error(
        "Failed to read extended profile: %s", e.what());
    return false;
  }
}

//------------------------------------------------------------------------------
bool ausf_bcf::register_to_bcf() {
  if (!ausf_cfg.register_bcf) {
    Logger::ausf_app().debug("BCF registration is disabled");
    return false;
  }

  Logger::ausf_app().info("[AUSF][BCF] Starting NF registration to BCF");
  Logger::ausf_app().info("[AUSF][BCF-REG] Preparing registration request to BCF");

  // Read extended profile from file (generated by DID Proxy)
  nlohmann::json extended_profile = {};
  if (!read_extended_profile_from_file(extended_profile)) {
    Logger::ausf_app().error(
        "[AUSF][BCF-REG] Failed to fetch extended profile from DID Proxy, "
        "skipping BCF registration");
    return false;
  }

  network_support::append_nf_profile_network_support(
      extended_profile, ausf_cfg.get_plmn_list());

  const std::string profile_nf_instance_id =
      extended_profile.value("nfInstanceId", ausf_instance_id);
  const std::string profile_nf_type =
      extended_profile.value("nfType", std::string("AUSF"));
  const std::string profile_did = extended_profile.value("did", std::string(""));

  Logger::ausf_app().info(
      "[AUSF][BCF] Registration context: nfInstanceId=%s nfType=%s did=%s",
      profile_nf_instance_id.c_str(), profile_nf_type.c_str(),
      profile_did.empty() ? "N/A" : profile_did.c_str());

  // Build BCF URI
  std::string bcf_uri = {};
  ausf_bcf_helper::get_bcf_nf_instance_uri(
      ausf_cfg.bcf_addr, ausf_instance_id, bcf_uri);

  std::string body = extended_profile.dump();

  // --- App layer: dispatch to client layer ---
  Logger::ausf_app().info(
      "[AUSF][BCF-REG] Sending registration message to HTTP client, "
      "bcf_uri=%s",
      bcf_uri.c_str());

  // --- Client layer: receive and process request ---
  Logger::ausf_client().info(
      "Receive BCF Register NF Instance Request, handling ...");
  Logger::ausf_client().info("BCF Registration URI: %s", bcf_uri.c_str());
  Logger::ausf_client().info("Send HTTP message to %s", bcf_uri.c_str());
  Logger::ausf_client().info("HTTP message Body: %s", body.c_str());

  // Send HTTP PUT request to BCF
  oai::http::request http_request =
      http_client_inst->prepare_json_request(bcf_uri, body);
  Logger::ausf_client().debug("Send a simple HTTP request");
  auto http_response = http_client_inst->send_http_request(
      oai::common::sbi::method_e::PUT, http_request);

  if (http_response.status_code ==
      oai::common::sbi::http_status_code::NO_RESPONSE) {
    Logger::ausf_client().error(
        "Cannot get response when calling %s", bcf_uri.c_str());
    Logger::ausf_app().error("[AUSF][BCF-REG] No response from BCF");
    return false;
  }

  // --- Client layer: response received ---
  Logger::ausf_client().info(
      "Get response with HTTP code (%d)",
      static_cast<int>(http_response.status_code));

  // Check response status
  if ((http_response.status_code == oai::common::sbi::http_status_code::OK) ||
      (http_response.status_code ==
       oai::common::sbi::http_status_code::CREATED) ||
      (http_response.status_code ==
       oai::common::sbi::http_status_code::NO_CONTENT)) {
    Logger::ausf_client().debug(
        "BCF Registration successful, got response from BCF");

    // --- App layer: received response from client layer ---
    Logger::ausf_app().debug(
        "Received BCF registration response from client layer");
    Logger::ausf_app().info(
        "[AUSF][BCF-REG] Registration response received from BCF");
    Logger::ausf_app().info(
        "[AUSF][BCF-REG] Registration SUCCESS at %s (HTTP %d)",
        bcf_uri.c_str(), static_cast<int>(http_response.status_code));
    Logger::ausf_app().info(
        "[AUSF][BCF] NF registration successful: nfInstanceId=%s nfType=%s did=%s",
        profile_nf_instance_id.c_str(), profile_nf_type.c_str(),
        profile_did.empty() ? "N/A" : profile_did.c_str());

    // Parse response (detailed JSON only at trace level)
    if (!http_response.body.empty()) {
      try {
        nlohmann::json response_data = nlohmann::json::parse(http_response.body);
        Logger::ausf_app().trace(
            "[AUSF][BCF-REG] Registration response: %s", response_data.dump(2).c_str());
      } catch (const nlohmann::json::exception& e) {
        Logger::ausf_app().trace("[AUSF][BCF-REG] Response is not JSON: %s", e.what());
      }
    }
    return true;
  } else {
    Logger::ausf_client().error(
        "BCF Registration failed with response code: %d",
        static_cast<int>(http_response.status_code));

    // --- App layer: received failed response from client layer ---
    Logger::ausf_app().debug(
        "Received BCF registration response from client layer");
    Logger::ausf_app().info(
        "[AUSF][BCF-REG] Registration response received from BCF");
    Logger::ausf_app().error(
        "[AUSF][BCF-REG] Registration FAILED at %s (HTTP %d)",
        bcf_uri.c_str(), static_cast<int>(http_response.status_code));
    Logger::ausf_app().error(
        "[AUSF][BCF] NF registration failed: nfInstanceId=%s nfType=%s did=%s",
        profile_nf_instance_id.c_str(), profile_nf_type.c_str(),
        profile_did.empty() ? "N/A" : profile_did.c_str());
    Logger::ausf_app().debug(
        "[AUSF][BCF-REG] Error response: %s", http_response.body.c_str());
    return false;
  }
}

//------------------------------------------------------------------------------
void ausf_bcf::update_bcf_registration() {
  if (!ausf_cfg.register_bcf) {
    Logger::ausf_app().debug("BCF registration is disabled");
    return;
  }

  Logger::ausf_app().info("[AUSF][BCF-REG] Preparing update request to BCF");

  // Read extended profile from file
  nlohmann::json extended_profile = {};
  if (!read_extended_profile_from_file(extended_profile)) {
    Logger::ausf_app().error(
        "[AUSF][BCF-REG] Failed to fetch extended profile from DID Proxy, "
        "cannot update registration");
    return;
  }

  // Build BCF URI
  std::string bcf_uri = {};
  ausf_bcf_helper::get_bcf_nf_instance_uri(
      ausf_cfg.bcf_addr, ausf_instance_id, bcf_uri);

  // Build PATCH items
  nlohmann::json patch_items = nlohmann::json::array();
  nlohmann::json patch_item  = {
      {"op", "replace"}, {"path", "/nfStatus"}, {"value", "REGISTERED"}};
  patch_items.push_back(patch_item);

  std::string body = patch_items.dump();

  // --- App layer: dispatch to client layer ---
  Logger::ausf_app().info(
      "[AUSF][BCF-REG] Sending update message to HTTP client, bcf_uri=%s",
      bcf_uri.c_str());

  // --- Client layer: receive and process request ---
  Logger::ausf_client().info(
      "Receive BCF Update NF Instance Request, handling ...");
  Logger::ausf_client().info("BCF Registration URI: %s", bcf_uri.c_str());
  Logger::ausf_client().info("Send HTTP message to %s", bcf_uri.c_str());
  Logger::ausf_client().info("HTTP message Body: %s", body.c_str());

  // Send HTTP PATCH request to BCF
  oai::http::request http_request =
      http_client_inst->prepare_json_request(bcf_uri, body);
  Logger::ausf_client().debug("Send a simple HTTP request");
  auto http_response = http_client_inst->send_http_request(
      oai::common::sbi::method_e::PATCH, http_request);

  if (http_response.status_code ==
      oai::common::sbi::http_status_code::NO_RESPONSE) {
    Logger::ausf_client().error(
        "Cannot get response when calling %s", bcf_uri.c_str());
    Logger::ausf_app().error("[AUSF][BCF-REG] No response from BCF");
    return;
  }

  // --- Client layer: response received ---
  Logger::ausf_client().info(
      "Get response with HTTP code (%d)",
      static_cast<int>(http_response.status_code));

  if ((http_response.status_code == oai::common::sbi::http_status_code::OK) ||
      (http_response.status_code ==
       oai::common::sbi::http_status_code::NO_CONTENT)) {
    Logger::ausf_client().debug(
        "BCF Update successful, got response from BCF");
    Logger::ausf_app().debug(
        "Received BCF update response from client layer");
    Logger::ausf_app().info(
        "[AUSF][BCF-REG] Update SUCCESS at %s (HTTP %d)",
        bcf_uri.c_str(), static_cast<int>(http_response.status_code));
  } else {
    Logger::ausf_client().error(
        "BCF Update failed with response code: %d",
        static_cast<int>(http_response.status_code));
    Logger::ausf_app().debug(
        "Received BCF update response from client layer");
    Logger::ausf_app().error(
        "[AUSF][BCF-REG] Update FAILED at %s (HTTP %d)",
        bcf_uri.c_str(), static_cast<int>(http_response.status_code));
  }
}

