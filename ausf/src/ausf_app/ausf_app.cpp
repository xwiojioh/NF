//------------------------------------------------------------------------------
ausf_app::ausf_app(const std::string& config_file, ausf_event& ev)
    : event_sub(ev),
      contextId2security_context(),
      supi2security_context(),
      imsi2security_context(),
      m_did_auth(nullptr),
      m_did_auth_enabled(false),
      m_security_audit(nullptr),
      m_bcf_state(BcfLifecycleState::INIT) {
  // Initialize BCF authentication module (if enabled in config)
  // This loads DID/keys but does NOT register or authenticate to BCF yet.
  // The actual flow is: constructor(init_did_auth_module()) → start() → register_to_bcf()
  //   → registration response → trigger_bcf_auth_after_registration()
  init_did_auth_module();
  init_security_audit_module();
}

//------------------------------------------------------------------------------
ausf_app::~ausf_app() {
  Logger::ausf_app().debug("Delete AUSF_APP instance...");
  
  // Cleanup BCF auth module
  if (m_did_auth) {
    m_did_auth.reset();
  }
  m_did_auth_enabled = false;
  
  if (ausf_bcf_inst) {
    delete ausf_bcf_inst;
    ausf_bcf_inst = nullptr;
  }
}

//------------------------------------------------------------------------------
bool ausf_app::start() {
  Logger::ausf_app().startup("Starting...");
  
  // Get AUSF instance ID from extended profile (generated by DID Proxy)
  std::string ausf_instance_id;
  if (!ausf_bcf::read_nf_instance_id_from_file(ausf_instance_id)) {
    ausf_instance_id = to_string(boost::uuids::random_generator()());
    Logger::ausf_app().debug(
        "Generated new AUSF instance ID: %s", ausf_instance_id.c_str());
  } else {
    Logger::ausf_app().debug(
        "Using AUSF instance ID from extended profile: %s",
        ausf_instance_id.c_str());
  }

  // Register to BCF (Blockchain Function) for DID-based identity
  // BCF has completely replaced NRF for NF registration and discovery
  // NOTE: init_did_auth_module() (BCF auth init) was already called in constructor.
  //       Auth is NOT triggered here — it waits for registration success callback.
  if (ausf_cfg.register_bcf) {
    try {
      ausf_bcf_inst = new ausf_bcf(event_sub, ausf_instance_id);

      // State transition: INIT/DID_READY → REGISTRATION_IN_PROGRESS
      {
        std::lock_guard<std::mutex> lock(m_bcf_state_mutex);
        m_bcf_state = BcfLifecycleState::REGISTRATION_IN_PROGRESS;
      }
      Logger::ausf_app().info(
          "[AUSF][BCF-REG] State → REGISTRATION_IN_PROGRESS, "
          "sending registration request to BCF");

      bool reg_success = ausf_bcf_inst->register_to_bcf();

      // --- App layer: received registration result from client layer ---
      Logger::ausf_app().info(
          "Received BCF_REGISTER_NF_INSTANCE_RESPONSE");

      if (reg_success) {
        // State transition: REGISTRATION_IN_PROGRESS → BCF_REGISTERED
        {
          std::lock_guard<std::mutex> lock(m_bcf_state_mutex);
          m_bcf_state = BcfLifecycleState::BCF_REGISTERED;
        }
        Logger::ausf_app().info(
            "[AUSF][BCF-REG] State → BCF_REGISTERED");

        if (m_security_audit) {
          m_security_audit->record_event(
              "NF_REGISTER", "register_response_received", "success", "",
              "BCF", "BCF", {{"nf_instance_id", ausf_instance_id}});
        }

        // Now that registration is confirmed, trigger BCF authentication
        // This is the ONLY place where auth is allowed to start after registration
        trigger_bcf_auth_after_registration();
      } else {
        Logger::ausf_app().error(
            "[AUSF][BCF-REG] Registration FAILED (see above for HTTP details)");
        {
          std::lock_guard<std::mutex> lock(m_bcf_state_mutex);
          m_bcf_state = BcfLifecycleState::FAILED;
        }
        Logger::ausf_app().error(
            "[AUSF][BCF-REG] State → FAILED, BCF authentication will NOT be attempted");
        if (m_security_audit) {
          m_security_audit->record_event(
              "NF_REGISTER", "register_response_received", "failure", "",
              "BCF", "BCF", {{"nf_instance_id", ausf_instance_id}});
        }
      }
    } catch (std::exception& e) {
      Logger::ausf_app().error(
          "[AUSF][BCF-REG] Exception during BCF registration: %s", e.what());
      {
        std::lock_guard<std::mutex> lock(m_bcf_state_mutex);
        m_bcf_state = BcfLifecycleState::FAILED;
      }
      // BCF registration failure should not prevent AUSF from starting
      Logger::ausf_app().warn(
          "[AUSF][BCF-REG] AUSF will continue without BCF registration");
    }
  }

  Logger::ausf_app().startup("Started");
  return true;
}

//------------------------------------------------------------------------------
void ausf_app::stop() {
  if (m_security_audit) {
    m_security_audit->record_event(
        "NF_DEREGISTER", "deregister_requested", "started", "", "BCF",
        "BCF");
    m_security_audit->finalize_all("ausf_shutdown");
  }

  // Cleanup BCF auth sessions
  cleanup_did_auth_sessions();
  
  // Deregister from BCF
  if (ausf_bcf_inst and ausf_cfg.register_bcf) {
    ausf_bcf_inst->deregister_from_bcf();
    delete ausf_bcf_inst;
    ausf_bcf_inst = nullptr;
  }
}

